    public Partido AgregarArbitros(Partido par, int contador) {

        File file = null;
        BufferedReader br = null;
        FileReader fr = null;

        try {
            file = new File("./src/Data/Arbitros.txt");
            fr = new FileReader(file);
            br = new BufferedReader(fr);

            String linea = "";
            String[] partes = new String[2];
            String nombre = "";
            String tipo = "";
            double peso = 0.0;

            int contadorwhile = 0;

            for (int i = 0; i < contador; i++) {
                linea = br.readLine();
            }
            while ((linea = br.readLine()) != null) {

                partes = linea.split(";");
                nombre = partes[0];
                tipo = partes[1];
                peso = Double.parseDouble(partes[2]);

                Arbitro arb = new Arbitro(tipo, nombre, peso);

                arb.setPartido(par);

                par.addArbitro(arb);

                if (contadorwhile == 4) {
                    break;
                }

                contadorwhile++;

            }
        } catch (Exception e) {
            e.printStackTrace();
            
        } finally{
            try {
                br.close();
                fr.close();
            } catch (Exception e) {
            }
        }

        return par;

    }

